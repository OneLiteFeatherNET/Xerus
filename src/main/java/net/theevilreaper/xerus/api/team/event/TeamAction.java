package net.theevilreaper.xerus.api.team.event;

/**
 * The {@link TeamAction} enum defines the possible actions that can be performed on a team.
 *
 * @author theEvilReaper
 * @version 1.0.0
 * @since 1.7.3
 */
public enum TeamAction {

    /**
     * Represents the action of adding a player to a team.
     */
    ADD,
    /**
     * Represents the action of removing a player from a team.
     */
    REMOVE;
}
